#define SYNTAX "syntax: dspam_crc [token]"

int
main (int argc, char *argv[])
{
  unsigned long long crc;

  if (argc < 2)
  {
    printf ("%s\n", SYNTAX);
    exit (EXIT_FAILURE);
  }

  crc = _ds_getcrc64 (argv[1]);

  printf ("TOKEN: '%s' CRC: %"LLU_FMT_SPEC"\n", argv[1], crc);
  exit (EXIT_SUCCESS);
}
